<strong>CHIPPERS STORY – A story of God’s love for all his creatures</strong> <br>
I think it important to let everyone who knows us how God answered our prayers for little Chipper. <br>
Chipper escaped from our kennel around 9:30 p.m. Monday evening. Martin was exercising the dogs one last time before tucking them in for the night. Chipper was loose in the far end of the security area. Martin opened the gate not thinking that Chipper had been waited for this opportunity. As soon as the gate was open he bolted, and before Martin could close the gate he was gone! <br>
This was so uncharacteristic of him. It shocked Martin, and he immediately went to the house to get my assistance to round him up. He first went after him, calling to him, but Chipper had a one track mind, and that was to run. It was a very cold but clear night, not full moon, but close to it. We hardly needed a flashlight to see. <br>
It wasn’t long before we realized we needed more help. I phoned our daughter Mary Jo who came out from Hyde Park to help in the search. Kim also came from Lucan to assist. We went our separate ways going to every direction with one of us always staying at the shelter in case Chipper returned on his own. We kept up the search for about 3 hours, and by that time we all were exhausted and needed to call it off. <br>
During my search it was very disconcerting to hear a pack of wolves or coyotes yip, yipping very close to us as I walked through the bush in the dark. Did the coyotes find Chipper? But, no I refused to believe that thought. We had prayed for God’s angels to protect and keep Chipper safe. <br>
It was difficult sleeping and Martin and I got up several times before staying up at 5:30 a.m. We had left all the outside lights on plus, the side door of the kennel was left so that a gentle nudge of a dog’s nose would have opened it. Chipper’s outside run and door were also left open in case he returned. We quickly went to see if he had returned, but alas no dog! Chipper was wearing London dog tags and I waited until close to 9 a.m. to call to report him lost. I gave the lost and found department all the details, his name, owner’s name, address, and the fact the owner was on vacation and had left the dog in our care. They were told how desperate we were in finding this little dog and to please call our shelter if they got any word.  <br>
Unbeknown to us, someone had called London Animal Care and Control around 8:00 a.m. They had found Chipper and wanted to return him to his owner. This is where a huge problem arose. The left hand did not know what the right hand was doing!  Two different people took the information in two different departments at Animal Control. The messages did not connect!   <br>
Around 2:00 p.m. our staff, once again on a whim, phoned Animal Care in London. To our surprise we were told, yes, someone had called and the dog Chipper had been reported found!  They refused to give us any more details because of privacy laws. Staff asked me if I could speak to the “head” person at London Animal Care to get some more co-operations. I knew Brett Harlton very well, who is the C.E.O. I was able to connect with him and give him our story. <br>
It still took about an hour for us to finally get a call from a woman who had Chipper. She actually hesitated when I told her I would be picking up Chipper. She wanted to keep the dog!  I convinced her; I had his coat and food and would like to come for him. She finally agreed. <br> 
I found out she had actually phoned Animal Care in London around 8:00 a.m.! Where was Chipper found?  I finally got the full story.  <br>
He had actually wandered two concessions down the highway from our shelter before running out of steam. These people have two beagles that have boarded at our kennel many times. Their daughter used to work for us years ago. The father first saw Chipper in their front yard around 5:30 a.m. He actually saw Chipper cross that crazy busy highway but return to their house!  It wasn’t until around 7:30 that their daughter saw Chipper in their back yard and got him inside. Their two beagles did not like the intruder, and she called her Mom who lives in Strathroy, to come take care of the dog. It was the mom who called London Animal Control to give them the tag number to find out who the owner might be. <br>
What astounds us is the fact this dog was able to survive temperatures ranging from minus 6 degrees Centigrade to zero for almost 12 hours! Chipper used to shiver when at our shelter, even in his coat!  One fact I need to tell you is that I was able to connect with the owner to tell them Chipper was lost. It was a most difficult conversation, as you can imagine. He told me Chipper was not road wise at all and that he did not believe this dog would ever survive such low temperatures! <br>
See what God has done! I firmly believe, God answered our prayers of sending an angel to guard over Chipper and that he would be returned to the kennel in good condition. I am humbled to think how much God loves us and the critters we love so dearly. Chipper’s story will always be to us a testimony of God’s great grace, mercy and love! <br>
Gertie
