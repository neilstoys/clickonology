               <p>Tribute to Tinker</p>
               <p> She was ours for exactly 6 months to the day. I picked her up as a little stray in the middle of the town of Strathroy, right by the Seven to Eleven, a scared timid little bag of bones she got to sit in my lap on the way to the shelter, no crate for this little soul. Little did I know that this shivering but vibrant boney pooch would be the one dog who right from that point on adopted me?</p>
               <p> We just had the vet come to our house to put down our faithful little Jack Russell Terrier, Jack, a dog of close to 16 years. He had always been the light of my life; I was not ready or even remotely interested in a new dog. My heart still ached at the loss of my mascot, Jack.<br>
               </p>
               <p> We listed her on the internet adoption site as Clementine, this was shortened to &ldquo;T&rdquo; and I fondly called her my Tinker Bell. Surprisingly, we did not have any response to our web ad. It was a week before Christmas, and the kennel was chalk full of boarding dogs. Where were we going to put this little shrimp? All the staff noticed how she took to me and encouraged me to take her into the house. Reluctantly, I did, just to make room for another boarding dog. I brought a little round cat bed from the cat room and placed it on top of our bed at my foot end. She curled up in it very content and happy to have finally arrived. She very obediently stayed there when told to. It was uncanny how she understood so quickly what was expected of her.</p>
               <p> It was soon very obvious to all that she was completely devoted to me. Why is still and always will be a mystery. I did not do anything that would endear her to me. She was the anamorphous one, always there to bring me her favourite squeaky toys, craving my attention. I started taking her with me on my dog pickups and errands. She would come and follow me when doing chores, walking the dogs or working at my desk. For her comfort I got a wicker basket to curl up in that just fit in the corner of my desk. The cats grew to accept her and it was comical to see how the two cats and Tinker would vie for my attention. At times all three of them would be contentedly, sleeping on my desk, while I worked. No matter how many times I would leave the desk, Tinker would jump off and follow. As soon as I sat down, she would wait for my cue, use my lap as a stepping stone, and glide effortlessly to the top of the desk.<br>
               </p>
<img src="image/htpics/tb5 480.jpg" width="480" height="446" border="0">			   
               <p> She came in weighing just barely ten pounds. When taking her to the vet for her heartworm check, she was over fourteen. This pathetic looking creature was now sleek, lithe and fully girdled with pure muscle, and fur that shone from lots of good food and treats. She must have survived on scraps and garbage before. I discovered there was almost no food she wouldn&rsquo;t eat. It was her pleasure to munch on apples and carrots with me, to happily chew on grapes, nuts, or popcorn. Anything I ate, she ate. What a remarkable dog!<br>
               </p>
               <p> The ironic thing is, now that she has been taken away from me, I am beginning to understand how much she meant to me, how much she taught me about life in her short life. We have a plaque on the wall in the shelter bathroom that lists, &ldquo;Things I learned from my dog.&rdquo; Tinker has given me a brand new list. Most of all she has taught me what true love and joy is and to appreciate the simple things in life. When I finally was ready to get up in the morning, she would roll on her back, stretching out her whole body and give me a contented little squeal. I would rub her tummy, give her just enough attention that enticed her to grab her squeaky toy and tell me, and&rdquo;Play time!&rdquo;<br>
               </p>
               <p> Who, but a dog, could love so unconditionally? No matter what the day would bring, Tinker helped put a perspective on each situation, that spark of joy filled her eyes, her whole being became alive with utter love and devotion when I stopped to pet and acknowledge her presence. She helped me to focus on what was really important. My spirit would soar with her spirit as we walked the shelter dogs around the trail together. Her body would weave its way through the green grass, drenched with the morning dew. It was her delight to chase the birds and butterflies, and travel ten times the distance I walked.</p>
               <p> It was pure unadulterated joy for her to taste freedom, yet still keep focused on me as we did the rounds over and over again with the shelter dogs in tow, morning and evening. Some dogs didn&rsquo;t go for her antics and she soon learned to keep her distance from certain dogs, others were enthralled at her attempts to play, &ldquo;Catch me if you can!&rdquo; Her unbridled exuberance and joy were infectious. She seized each moment and followed her heart.</p>
               <p> Oh, Tinker, thanks! May we all seize the days; God gives us with this kind of zeal! You taught me what friendship is all about, your selflessness, and unwavering loyalty stands out like a beacon. You may be gone, but you will always live on in my heart. Once in a while a dog comes along that is different, special. You were that kind of dog. I will never ever forget you! I may have dug that hole and buried your body with your favourite squeaky toy, but you are not there. Your spirit lives on and I can look foreward to the great reunion in the sky, with the one who graciously allowed you to live with me for these last six months. Six months where I was privileged to have you at my side, slowly and ever so gently giving all of yourself to me, giving me time to give all of myself to you. You have left a permanent mark on my life! There will never be another dog like you Tinker Bell.</p>
               <p> I miss you terribly and when those around me notice my eyes become misty, and my voice falter and fail me at the mention of your name, may they know it is only because of how dear and special you were and are and always will be. You may just have been a dog that someone cast off as garbage. To me and thousands of others who have taken in shelter dogs we know you are a treasure that has no price!</p>
               <p> What we learn from our shelter dogs is that contentment is not found in our fancy houses, cars or designer clothes. A new squeaky toy will do just fine! These dogs do not judge us by our colour or creed or class but who we are inside. This is what really and truly matters and what makes the difference in a life well lived. Seize the day! Bring a shelter dog into your life! This castaway can become your best friend, teacher and companion. Just don&rsquo;t make the same mistake I made with Tinker. I didn&rsquo;t fully realize the gift she was until she was taken away from me. Give your dog the love and attention they deserve while you still have them with you. Life is so fragile and short to allow crazy unimportant negative thoughts and experiences to rule us. Learn what is really important in life by looking at your dog, a gift to you for only a short while. Give your dog that hug for me today, in memory of my dear little Tinker Bell.</p>
               <p> .....Gertie</p>
