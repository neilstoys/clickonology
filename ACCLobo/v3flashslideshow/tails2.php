Hi, Guys,<br>
 
I wanted to let you know that Rikki (now CoCo) is doing fabulously.
We took her to the vet Saturday morning.  
As it turns out, the spot on her hip she was gnawing at turned out to be a pretty nasty skin infection from a previous flea infestation.
She also had an ear infection, but I'm happy to say she's otherwise very healthy.
She's getting meds for both issues, and aside from a weird-looking bald spot on her butt wher the vet shaved her to get a better look at the infection, she's a-ok.<br>
 
In the week since we've had her, she's doing terrifically.<br>
We figured out after 3 accidents that she's got a submissive urination issue, but that's been easily rectified by doing her training outside.
She's learned "sit" already, and can "heel" for about four to six steps.  We're working on "lay down" and "stay" now.
She knows to sit to get her leash on, to sit to have it taken off, and she's learned her release command, "OK".
She's learned how to walk on the leash without lunging all over the place, and to not bull her way through the door going out and coming in.
She has snapped at a couple of dogs when we've been out for walks, but she's been really good with others, so it's too soon to tell if she's got some fear aggression or if the dogs that got snapped at just got too close to the wound on her hip.  Whichever it is, we'll help her get over it.<br>
 
Anyway, I just wanted to let you know that her sweet, eager-to-please nature is a very, very good fit with us, and she's adjusting very well to her new family.<br>
 
<p>June 05<br>
Hello, Gertie,<br>
 
Just wanted to give you a quick update on Coco.<br>
 
She's doing terrific!  She's become very secure and trusting of us, which makes me very, very happy to see.<br>
She's up to about 65 lbs now, almost at a perfect weight.  She's not bolting her food any more, though you'll hear the occasional belch out of her when she gulps a lot of water.   
Her fur's almost all grown back where the vet shaved her, and no more ear infections since that first one.  Her heartworm tests came back all clear, too.  She's even gotten healthy enough to start shedding ... it's too bad I don't know how to card wool, 'cause I SWEAR we took another whole dog's worth of fur off her in the last month!   <br>
 
Her training is coming along well ... she didn't like the airline carrier we got her her as a crate, but she's finally accepted it as her 'den'.  Now she sleeps in it, and when she gets a treat, it's straight to the den to enjoy it.  As it turns out, she's totally fine in the house alone for a few hours, so we've not needed to actually crate her, but we have enough space to leave it for her to use.
She sits and lays down on command, and her recall is getting better every day.  She's even getting "bang bang", 'cause she gets a belly rub when she rolls over on her back on that command.<br>
 
She's definitely a retreiver ... she doesn't just chase a ball, she bounds after it.  Picture a 65-lb black rabbit leaping across the grass, and you`ve got the visual!  She got "fetch" with minimal prompting, thought getting her to give the ball back is a bit harder.  When I started teaching her "drop it", I rolled a ball for her, and she brought it back, but didn't want to let it go.  I took a treat out of my pocket, and told her to 'drop it' again.  She promptly took the ball to her crate, dropped it there, then came back and sat for the treat.  I almost busted a gut laughing.<br>
 
Amazingly enough, she's picking up hand signals.  I got in the habit of snapping my fingers to get her attention when we started training her, and I noticed one day that she sat when I snapped and pointed at the floor before I gave a command.  So we're working on those, too. <br>
 
We've also discovered that she's a chewer, after she gnawed on a couple of kitchen chairs.   She went through rawhide chews like they were tissue paper, and I found the butcher bones splintered and were stinky.  So we tried a piece of deer antler, and she LOVED it.  It took her two months to wear down the first 12" piece to a nub, and when I mentioned it to my friend ... well, her dad's a hunter and I attached pictures of what she brought in to my work to take home to Coco.  It looks like one of those freaky sculptures from Beetlejuice.  She can't hardly carry the damn thing, but she sure tries!<br>
 
As it turns out, she loves the water.  No issues at all with bath time, plus she's fascinated with the neighbor's pool.  When it was really hot last week, I filled a small storage tub with cool water for her feet.  It was really easy to get teach her to step into it.  The first time, she looked at me like she was asking if that was what I wanted.  I praised her, then told her to 'step out'.  Which she did.   When I told her to 'step in', she was right back in it, and when I praised her, she started splashing around like a kid in a pool!  It was hilarious.  She won't do it with puddles (thank God ... the mud that gets in those webbed toes is incredible!) ... just in the tub.<br>
 
I have to say, I'm very, very glad Fate prompted the lady (I'm sorry, I can't remember her name ... very petite with dark hair and blue eyes) to lament that everyone was coming to see the Rottie and nobody wanted to see Coco.  I hope that Rottie found a good home, too, but I'm glad we took Coco instead.  She is the very definition of a `good dog`, and we love her very, very much.<br>
Beth

					
