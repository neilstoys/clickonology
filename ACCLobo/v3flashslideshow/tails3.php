June 5th 2013<br>
Our story started after adoption of Bing by Sarah Burn and Bart Howell (above). Bing was diagnosed with heartworm and faced certain death.<br>
A.C.C.Lobo had not one, but two dogs that came in as stray earlier this year that tested positive for Heartworm. Most shelters would have had these beautiful dogs put down as the cost is too great. Our vet charged $1000.00 per dog for treatment.<br>
<span class="style1">We put out a plea on our website for Bing, and with generous donations of our supporters raised enough money to cover the cost for Bing.<br>
A.C.CLobo decided to match the donations and also pay for Sam’s treatment. </span>
<p><span class="style1">
Thanks to ALL for your help with this worthy cause!</span><br>
Gertie and Martin Dieleman owners,         Animal Care Centre Lobo</p>

