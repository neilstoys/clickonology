<p>August 28th 2013<br>		
Hi Gertie,</p>

Just two months ago, we adopted Bing (now Arnie). <span class="style1">We wanted you and your
staff to know how happy we are</span>! He is a great dog, and so smart!<br>

When we adopted him he had a high count of heartworm and you and your staff
did an amazing thing for us, raising donations to pay for his very expensive
treatment. He has had his third and final injection now, and is dong
unbelievably well! He is only 11 months old, so keeping him calm while the
treatment takes effect has been a challenge; but we're doing it!<br>

He was very scared of cats when at the shelter, but has really warmed up to
his new sister cat, Lucy. He really wants her to play with him, but she has
not quite accepted the offer yet, ha. Lucy has been teaching Arnie her bad
kitty tricks, like sneaking out the door and sitting on the back of the
couch!<br>

We are so happy to have Arnie in our lives and don't know what we do without
him. <span class="style1">Thank you so much for all you and your staff have done to help us bring
Arnie home healthy</span>. We hope to pay it forward soon and help another
family/couple rescue a beautiful animal that needs our help.<br>

<p>Thank you,<br>
Sarah Burn, Bart Howell, Arnie and Lucy.	</p>			</td>
